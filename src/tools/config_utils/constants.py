import math

# ── 格子玻爾茲曼聲速 ──────────────────────────────────────────────────────
CS2 = 1.0 / 3.0
CS = math.sqrt(CS2)  # ≈ 0.5774 lu/step (lattice sound speed)

# ── 入口馬赫數上限 ────────────────────────────────────────────────────────
MA_LIMIT = 0.17  # 入口均值 Ma 上限（對應 u ≈ 0.098 lu/step）

# ── LBM 鬆弛時間下限 ──────────────────────────────────────────────────────
TAU_MIN = 0.52  # tau 下限（對應 nu_lb_min ≈ 0.0067）

# ── 步數估算保守係數 ──────────────────────────────────────────────────────
U_STEP_FACTOR = 0.6  # Bernoulli 高估修正：保守速度 = u_bernoulli × 0.6

# ── 阻塞率感知參數 ────────────────────────────────────────────────────────
U_GAP_MAX = 0.15  # 建築間隙最大安全速度（留 40% 餘量到 0.25 崩潰閾值）
MIN_OPEN = 0.20   # 最小開放比（防極端阻塞時分母趨近 0）

# ── Reynolds 數上限 ───────────────────────────────────────────────────────
RE_MAX = 1200  # Re 上限：超過此值在 2D LBM 都市場景數值不穩定
